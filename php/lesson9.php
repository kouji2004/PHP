for文を用いて、51から100までの数字をechoしてください。

※echoするときに改行も入れるようにしてください。

ヒント(改行)
echo $i.'<br>';
もしくは
echo $i;
echo '<br>';
とすることで改行できます。




  <?php

    // この下にfor文を書いてください
    for($i=51;$i<=100;$i++){
      echo $i;
      echo"<br>";
    }
    
  ?>

